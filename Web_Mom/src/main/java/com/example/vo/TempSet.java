package com.example.vo;

import java.util.List;

public class TempSet {
   private int totalCnt;
   private int resultCode;
   private String resultMsg;
   private List<?> rows;
   private int totalPage;
   private int firstPageOnPageList;
   private int lastPageOnPageList;
   
	@Override
	public String toString() {
		return "TempSet [totalCnt=" + totalCnt + ", resultCode=" + resultCode + ", resultMsg=" + resultMsg + ", rows="
				+ rows + ", totalPage=" + totalPage + ", firstPageOnPageList=" + firstPageOnPageList
				+ ", lastPageOnPageList=" + lastPageOnPageList + "]";
	}
	public int getTotalCnt() {
		return totalCnt;
	}
	public void setTotalCnt(int totalCnt) {
		this.totalCnt = totalCnt;
	}
	public int getResultCode() {
		return resultCode;
	}
	public void setResultCode(int resultCode) {
		this.resultCode = resultCode;
	}
	public String getResultMsg() {
		return resultMsg;
	}
	public void setResultMsg(String resultMsg) {
		this.resultMsg = resultMsg;
	}
	public List<?> getRows() {
		return rows;
	}
	public void setRows(List<?> rows) {
		this.rows = rows;
	}
	public int getTotalPage() {
		return totalPage;
	}
	public void setTotalPage(int totalPage) {
		this.totalPage = totalPage;
	}
	public int getFirstPageOnPageList() {
		return firstPageOnPageList;
	}
	public void setFirstPageOnPageList(int firstPageOnPageList) {
		this.firstPageOnPageList = firstPageOnPageList;
	}
	public int getLastPageOnPageList() {
		return lastPageOnPageList;
	}
	public void setLastPageOnPageList(int lastPageOnPageList) {
		this.lastPageOnPageList = lastPageOnPageList;
	}
}