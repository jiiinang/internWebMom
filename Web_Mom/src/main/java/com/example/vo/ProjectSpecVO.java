package com.example.vo;

public class ProjectSpecVO {
	private String projId;
	private String memberCd;
	private String projSpecPlan;
	private String projSpecPerf;
	public String getProjId() {
		return projId;
	}
	public void setProjId(String projId) {
		this.projId = projId;
	}
	public String getMemberCd() {
		return memberCd;
	}
	public void setMemberCd(String memberCd) {
		this.memberCd = memberCd;
	}
	public String getProjSpecPlan() {
		return projSpecPlan;
	}
	public void setProjSpecPlan(String projSpecPlan) {
		this.projSpecPlan = projSpecPlan;
	}
	public String getProjSpecPerf() {
		return projSpecPerf;
	}
	public void setProjSpecPerf(String projSpecPerf) {
		this.projSpecPerf = projSpecPerf;
	}
	
}
